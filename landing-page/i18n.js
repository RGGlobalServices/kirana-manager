/* ── Current locale ─────────────────────────────────── */
let _locale = localStorage.getItem('ks_language') || 'en';
if (!['en','hi','mr'].includes(_locale)) _locale = 'en';

function setLocale(loc) {
  _locale = loc;
  localStorage.setItem('ks_language', loc);
  document.documentElement.lang = loc;
}

function getLocale() { return _locale; }

/* ── Translate a key ────────────────────────────────── */
function __(key) {
  const t = TR[key];
  if (!t) return key;
  return t[_locale] || t.en || key;
}

/* ── Translate all data-i18n elements on the page ──── */
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = __(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = __(key);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    el.title = __(key);
  });
}

/* ── Build language switcher HTML ───────────────────── */
function langSwitcherHTML() {
  let html = '<div class="lang-switcher"><button class="lang-btn" id="langBtn">';
  html += LANG[_locale].flag + ' ' + LANG[_locale].name;
  html += ' <span class="lang-arrow">▾</span></button><div class="lang-dropdown" id="langDropdown">';
  for (const [code, l] of Object.entries(LANG)) {
    html += `<a href="#" class="lang-opt${code === _locale ? ' active' : ''}" data-lang="${code}">${l.flag} ${l.name}</a>`;
  }
  html += '</div></div>';
  return html;
}

/* ── Init language switcher behavior ────────────────── */
function initLangSwitcher() {
  const btn = document.getElementById('langBtn');
  const drop = document.getElementById('langDropdown');
  if (!btn || !drop) return;
  btn.addEventListener('click', (e) => {
    e.stopPropagation();
    drop.style.display = drop.style.display === 'block' ? 'none' : 'block';
  });
  drop.querySelectorAll('.lang-opt').forEach(el => {
    el.addEventListener('click', (e) => {
      e.preventDefault();
      const code = el.getAttribute('data-lang');
      setLocale(code);
      applyTranslations();
      // Update switcher text
      btn.innerHTML = LANG[code].flag + ' ' + LANG[code].name + ' <span class="lang-arrow">▾</span>';
      drop.querySelectorAll('.lang-opt').forEach(o => o.classList.toggle('active', o.getAttribute('data-lang') === code));
      drop.style.display = 'none';
      // Save for other pages
      window.dispatchEvent(new CustomEvent('localeChanged', { detail: code }));
    });
  });
  document.addEventListener('click', () => { if (drop) drop.style.display = 'none'; });
}

/* ── One-shot init for language selector + translate ── */
function initI18n() {
  const stored = localStorage.getItem('ks_language') || 'en';
  setLocale(stored);

  // Inject language switcher into navbar
  const navCta = document.querySelector('.nav-cta');
  if (navCta) {
    const wrapper = document.createElement('div');
    wrapper.id = 'langWrapper';
    wrapper.innerHTML = langSwitcherHTML();
    navCta.parentNode.insertBefore(wrapper, navCta);
  }

  applyTranslations();
  initLangSwitcher();
}

// Auto-init on DOM ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initI18n);
} else {
  initI18n();
}
